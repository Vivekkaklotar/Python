                                                        # Arithmetic operators

                                                        #operators :
                                                        # + (addition)
                                                        # - (subtraction)
                                                        # * (multiplication)
                                                        # / (division)
                                                        # % (modulus)
                                                        # ** (exponentiation)
                                                        # // (floor division)

# Example usage:
a = 10
b = 20
print(a + b)  # Output: 30
print(a - b)  # Output: -10
print(a * b)  # Output: 200
print(a / b)  # Output: 0.5              #10 divided by 20 gives 0.5   
print(a % b)  # Output: 10               #10 divided by 21 gives a remainder of 10
print(a ** 2) # Output: 100              #10 raised to the power of 2
print(a // 3) # Output: 3                #10 divided by 3 gives 3 (floor division)

                                                        #Assignment operators

                                                        #operators :
                                                        # = (assignment)
                                                        # += (add and assign)
                                                        # -= (subtract and assign)
                                                        # *= (multiply and assign)
                                                        # /= (divide and assign)
                                                        # %= (modulus and assign)
                                                        # **= (exponentiation and assign)
                                                        # //= (floor division and assign)

# Example usage:
a = 10
a += 5   # equivalent to a = a + 5
print(a)  # Output: 15  
a -= 3   # equivalent to a = a - 3
print(a)  # Output: 12
a *= 2   # equivalent to a = a * 2
print(a)  # Output: 24
a /= 4   # equivalent to a = a / 4
print(a)  # Output: 6.0
a %= 5   # equivalent to a = a % 5
print(a)  # Output: 1.0
a **= 3  # equivalent to a = a ** 3
print(a)  # Output: 1.0
a //= 2  # equivalent to a = a // 2
print(a)  # Output: 0.0

                                                        #Logical operators

                                                        #operators :
                                                        # and (logical AND)
                                                        # or (logical OR)
                                                        # not (logical NOT)

# Example usage:
a = True
b = False
print(a and b)  # Output: False
print(a or b)   # Output: True
print(not a)    # Output: False
print(not b)    # Output: True

                                                        #Comparison operators

                                                        #operators :
                                                        # == (equal to)
                                                        # != (not equal to)
                                                        # > (greater than)
                                                        # < (less than)
                                                        # >= (greater than or equal to)
                                                        # <= (less than or equal to)

# Example usage:
a = 10
b = 20
print(a == b)  # Output: False
print(a != b)  # Output: True
print(a > b)   # Output: False
print(a < b)   # Output: True
print(a >= b)  # Output: False
print(a <= b)  # Output: True


